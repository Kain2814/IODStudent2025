// ------------------------------------ Sketches (Slide 7) --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Sketches are quick, hand-drawn wireframe representations of the app's interface
// ----- focusing on layout on functionality rather than aesthetics.

// They allow designers to brainstorm and iterate ideas rapidly,
// ----- laying the groundwork for more refined designs.

// ANY TIME you spend on your application MUST create VALUE.
// ----- If the value you create in 5 mins is the same as 2 hours, then you should only spend 5 mins.

// What is value? - anything that helps you move foward. In early stages, pretty pictures don't do that.


// ------------------------------------- Lo-Fi Designs (Slide 8) -------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


// Lo-Fi is the key to acceptance, and also shows that you are proffesionally trained.
// ----- Low Fidelity should be as Low as possible.
// (Hint:) - don't use colors, in fact, they should be ugly, as plain and ugly as possible
// ----- Because you are showcasing FUNCTIONALITIES. 

// When people see your lo-fi, you don't need to explain it is lo-fi.
// ----- The moment you try to make lo-fi look attractive, you will move the attention 
// from functionality to look and feel. YOU DONT WANT THAT!

// Sites to look at for wireframing: Balsamiq(sub required), Moqups (free), Figma (idk yet)

// ------------------------------------- Paper prototyping (slide 9) ---------------------------------------------------------------------------------------------------------------------------------------------------------------------

// Paper prototyping involves design teams creating paper versions of digitial products
// ----- to understand concepts and test designs.
// They draw sketches or adapt printed materials for low-fidelity samples to guid designs and study user reactions early on.

// Even in the high-tech digital UX world, pen and paper remain favored for quick low-fidelity prototyping.
// ----- UX teams spend time away from computers, using sticky notes, whiteboards, and notepads to annotate paper prototypes.

// Effective planning lets designers swiftly create wireframes, mockups, and prototypes after this preliminary phase.
// Paper prototyping is crucial for early UX design as it encourages
// ----- collaboration and cost-effective exploration of various ideas.

